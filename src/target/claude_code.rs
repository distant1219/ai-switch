use crate::target::TargetAdapter;
use crate::types::{Provider, Target};
use anyhow::{Context, Result};
use std::fs;
use std::path::Path;

pub struct ClaudeCodeAdapter;

impl TargetAdapter for ClaudeCodeAdapter {
    fn apply(&self, provider: &Provider, target: &Target) -> Result<()> {
        let config_path = shellexpand::tilde(&target.config_path).to_string();
        let path = Path::new(&config_path);

        // Ensure parent directory exists
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)
                .with_context(|| format!("Failed to create directory {:?}", parent))?;
        }

        // Generate Claude Code configuration content
        let content = generate_config(provider);

        fs::write(path, content)
            .with_context(|| format!("Failed to write config to {:?}", path))?;

        println!("Updated Claude Code configuration at {}", config_path);
        Ok(())
    }
}

fn generate_config(provider: &Provider) -> String {
    let mut lines = vec![
        "# Claude Code Configuration".to_string(),
        "# Generated by ai-switch".to_string(),
        "".to_string(),
    ];

    lines.push(format!("CLAUDE_API_KEY={}", provider.api_key));

    if let Some(url) = &provider.base_url {
        lines.push(format!("CLAUDE_API_BASE_URL={}", url));
    }

    if let Some(model) = &provider.model {
        lines.push(format!("CLAUDE_MODEL={}", model));
    }

    lines.join("\n")
}
